def playblast_jpg_sequence(filepath, *args, **kwargs):
	# Time
	start = cmds.playbackOptions(q=True, minTime=True)
	end = cmds.playbackOptions(q=True, maxTime=True)
	
	# Resolution
	width = cmds.getAttr('defaultResolution.width')
	height = cmds.getAttr('defaultResolution.height')
	
	result = cmds.playblast(format='image',
	                        filename=filepath,
	                        compression='jpg',
	                        quality=100,
	                        showOrnaments=0,
	                        offScreen=1,
	                        percent=100,
	                        viewer=0,
	                        startTime=start,
	                        endTime=end,
	                        widthHeight=[width, height]
	                        )
	return result


