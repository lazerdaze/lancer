def listCheck(var, *args):
	if type(var) is str or type(var) is unicode:
		var = [str(var)]

	return var


def getSelected(*args):
	selected = cmds.ls(sl=True)

	if not selected:
		cmds.warning('Nothing selected.')

	return selected


def forwardAxis(obj, axis=[0, 0, 0], *args):
	x = axis[0] * 90
	y = axis[1] * 90
	z = axis[2] * 90
	var = [z, y, x]
	cmds.xform(obj, ro=var)
	freezeTransform(obj)
	return


